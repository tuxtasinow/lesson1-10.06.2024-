example = 'hamster'

print(example[0])  #Вывод первого символа строки

print(example[-1])  #Вывод последнего символа строки отрицательным индексом

print(example[2:])  #Вывод второй нечетной половины строки

print(example[::-1])  #Вывод строки наоборот

print(example[1::2])  #Вывод каждого второго символа